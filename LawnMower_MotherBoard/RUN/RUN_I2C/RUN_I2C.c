void RUN_I2C_Init()
{
	HAL_I2C_Init();
	HAL_I2C_AccelInit();
	HAL_I2C_CompassInit();

	ge_i2cUsed = E_I2C_USED_NONE;
}

void RUN_I2C_SetUsed(E_I2C_USED e_i2cUsed)
{
	ge_i2cUsed = e_i2cUsed;
}

E_I2C_USED RUN_I2C_GetUsed(void)
{
	return ge_i2cUsed;
}
